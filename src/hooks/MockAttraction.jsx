export const getAttractionById = (id) => {
  return mockAttractions.find(attraction => attraction.id === parseInt(id));
};

export const getFilteredAttractions = (filters, sortBy) => {
  let filteredAttractions = [...mockAttractions];

  filteredAttractions = filteredAttractions.filter(attraction => {
    const categoryMatch = Object.entries(filters).some(([key, value]) => 
      value && attraction.categories.includes(key) && key !== 'province'
    );

    const provinceMatch = !filters.province || attraction.province === filters.province;

    return categoryMatch || provinceMatch;
  });

  // Apply sorting
  switch (sortBy) {
    case 'popularity':
      filteredAttractions.sort((a, b) => b.rating.vote - a.rating.vote);
      break;
    case 'rating':
      filteredAttractions.sort((a, b) => b.rating.rate - a.rating.rate);
      break;
    case 'name':
      filteredAttractions.sort((a, b) => a.name.localeCompare(b.name));
      break;
    default:
      break;
  }

  return filteredAttractions;
};