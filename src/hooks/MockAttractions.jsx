export const getFilteredAttractions = (filters, sortBy) => {
  let filteredAttractions = [...mockAttractions];

  // Apply filters
  if (filters) {
    if (filters.name) {
      filteredAttractions = filteredAttractions.filter(attraction => 
        attraction.name.toLowerCase().includes(filters.name.toLowerCase())
      );
    }
    if (filters.type) {
      filteredAttractions = filteredAttractions.filter(attraction => 
        attraction.attractionTypeId === filters.type
      );
    }
    if (filters.status) {
      filteredAttractions = filteredAttractions.filter(attraction => 
        attraction.status === filters.status
      );
    }
    if (filters.rating) {
      filteredAttractions = filteredAttractions.filter(attraction => 
        attraction.Rating >= filters.rating
      );
    }
    if (filters.priceRange) {
      filteredAttractions = filteredAttractions.filter(attraction => {
        const price = parseFloat(attraction.TicketPrice.replace(/[^0-9.-]+/g,""));
        return price >= filters.priceRange[0] && price <= filters.priceRange[1];
      });
    }
  }

  // Apply sorting
  if (sortBy) {
    filteredAttractions.sort((a, b) => {
      if (sortBy === 'name') {
        return a.name.localeCompare(b.name);
      } else if (sortBy === 'date') {
        return b.createdDate - a.createdDate;
      } else if (sortBy === 'rating') {
        return b.Rating - a.Rating;
      } else if (sortBy === 'popularity') {
        return b.ReviewCount - a.ReviewCount;
      }
      // Add more sorting options as needed
    });
  }

  return filteredAttractions;
};

export const getPopularAttractions = (limit = 5) => {
  return [...mockAttractions]
    .sort((a, b) => b.ReviewCount - a.ReviewCount)
    .slice(0, limit);
};